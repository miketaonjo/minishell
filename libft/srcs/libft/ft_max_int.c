int	ft_max_int(int *tab, int len)
{
	int	i;
	int	m_int;

	i = 0;
	m_int = tab[0];
	while (i < len)
	{
		if (tab[i] > m_int)
			m_int = tab[i];
		i++;
	}
	return (m_int);
}
